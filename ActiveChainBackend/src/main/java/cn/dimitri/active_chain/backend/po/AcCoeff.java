package cn.dimitri.active_chain.backend.po;

public class AcCoeff {
    private double maleCoeff;
    private double femaleCoeff;
    private double fitCoeff;
    private double fatCoeff;
    private double fitFat;

    public double getMaleCoeff() {
        return maleCoeff;
    }

    public void setMaleCoeff(double maleCoeff) {
        this.maleCoeff = maleCoeff;
    }

    public double getFemaleCoeff() {
        return femaleCoeff;
    }

    public void setFemaleCoeff(double femaleCoeff) {
        this.femaleCoeff = femaleCoeff;
    }

    public double getFitCoeff() {
        return fitCoeff;
    }

    public void setFitCoeff(double fitCoeff) {
        this.fitCoeff = fitCoeff;
    }

    public double getFatCoeff() {
        return fatCoeff;
    }

    public void setFatCoeff(double fatCoeff) {
        this.fatCoeff = fatCoeff;
    }

    public double getFitFat() {
        return fitFat;
    }

    public void setFitFat(double fitFat) {
        this.fitFat = fitFat;
    }
}
